// abs : 절대값 반환
// sqrt : 제곱근 반환
// pow : 거듭제곱 계산
// exp : 지수 함수(e^x) 계산
// log : 자연 로그 계산
// sin , cos , tan : 삼각 함수 계산
// asin , acos , atan : 역삼각 함수 계산
// ceil : 올림
// floor : 내림
// round : 반올림

#include <cmath>

int main(int argc, char const *argv[])
{
  double x = 3.151;
  double y = 2.1561;

  abs(x);
  ceil(x);
  floor(y);
  round(x);
  return 0;
}
